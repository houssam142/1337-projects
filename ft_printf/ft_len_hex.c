#include "ft_printf_bonus.h"

int		ft_len_hex(unsigned long nb)
{
	int i;

	i = 0;
	if (nb == 0)
	{
		return (1);
	}
	while (nb != 0)
	{
		i++;
		nb /= 16;
	}
	return (i);
}
