#include "../include/cub3d.h"

void	ft_bzero(void *s, size_t len)
{
	size_t	i;
	char	*ptr;

	ptr = (char *)s;
	i = 0;
	while (i < len)
	{
		ptr[i] = 0;
		i++;
	}	
}
